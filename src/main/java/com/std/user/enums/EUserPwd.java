public enum EUserPwd {
    InitPwd("888888", "初始化密码1"), InitPwd16("123456", "初始化密码2");

    EUserPwd(String code, String value) {
        this.code = code;
        this.value = value;
    }

    private String code;

    private String value;

    public String getCode() {
        return code;
    }

    public String getValue() {
        return value;
    }
}
